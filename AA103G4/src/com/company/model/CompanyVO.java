package com.company.model;

public class CompanyVO implements java.io.Serializable {
	private Integer comp_no;
	private String comp_name;
	private String comp_tel;
	private String comp_add;
	private String comp_number;
	private String comp_contact;
	private String comp_email;

	public Integer getComp_no() {
		return comp_no;
	}

	public void setComp_no(Integer comp_no) {
		this.comp_no = comp_no;
	}

	public String getComp_name() {
		return comp_name;
	}

	public void setComp_name(String comp_name) {
		this.comp_name = comp_name;
	}

	public String getComp_tel() {
		return comp_tel;
	}

	public void setComp_tel(String comp_tel) {
		this.comp_tel = comp_tel;
	}

	public String getComp_add() {
		return comp_add;
	}

	public void setComp_add(String comp_add) {
		this.comp_add = comp_add;
	}

	public String getComp_number() {
		return comp_number;
	}

	public void setComp_number(String comp_number) {
		this.comp_number = comp_number;
	}

	public String getComp_contact() {
		return comp_contact;
	}

	public void setComp_contact(String comp_contact) {
		this.comp_contact = comp_contact;
	}

	public String getComp_email() {
		return comp_email;
	}

	public void setComp_email(String comp_email) {
		this.comp_email = comp_email;
	}

}
